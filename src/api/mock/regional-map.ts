export function getMockIndicatorSystemSummary(): IndicatorSystemSummary {
  const activeCount = rankingIndicators.filter((i) => i.status === 'active').length
  const dimensionCounts: Record<string, number> = {}
  rankingIndicators.forEach((i) => {
    dimensionCounts[i.dimension] = (dimensionCounts[i.dimension] || 0) + 1
  })

  return {
    kpiCards: {
      totalIndicators: rankingIndicators.length,
      dimensions: Object.keys(dimensionCounts).length,
      quantitative: activeCount,
      coverageRate: 96.8,
    },
    indicators: rankingIndicators,
    dimensionDistribution: Object.entries(dimensionCounts).map(([dim, count]) => ({
      name: DIMENSION_LABEL[dim as IndicatorDimension],
      value: count,
    })),
    weightDistribution: Object.entries(DIMENSION_LABEL).map(([dim, label]) => ({
      dimension: label,
      weight: dim === 'economic' ? 40 : dim === 'innovation' ? 30 : dim === 'social' ? 20 : 10,
    })),
  }
}

